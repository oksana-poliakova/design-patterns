// MARK: - Pattern classification

/*
 1) Generative patterns worry about flexible creation objects without introducing unnecessary dependencies into the program.
 
 A list of generating design patterns that are responsible for the convenient and safe creation of new objects or even entire families of objects.
 
    • Factory method (defines a common interface for creating objects in a superclass, allowing subclasses to change the type of objects they create)
    • Builder (defines a common interface for creating objects in a superclass, allowing subclasses to change the type of objects they create)
    • Prototype (allows you to copy objects without going into details their implementation)
    • Singleton (ensures that the class has only one instance, and provides sets a global access point to it)
 
 • Structural patterns show different ways building links between objects.
 • Behavioral patterns take care of effective communication connections between objects. */
